#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*destiny;
	size_t	source_length;

	source_length = ft_strlen(s) + 1;
	destiny = (char *)malloc(source_length);
	if (destiny)
		ft_strlcpy(destiny, s, source_length);
	return (destiny);
}
